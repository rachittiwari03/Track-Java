public class Conditional {
    public static void main(String[] args) {
        /*
        Conditional Statements are following

        1. If else
        2. else if
        3. ternary operator
        4. Switch
         */


        /*
        1. If else (boilerplate)

        If(Condition) {
        System.out.println(..........)
        }
        else{
        System.out.println(..........)
        }
         */



        /*
        2. else if (boilerplate)

        If(Condition1) {
        System.out.println(..........)
        }
        else if(Condition2) {
        System.out.println(..........)
        }
        else{
        System.out.println(..........)
        }

        note --- here if the first if condition is true then compiler will not check the next else if(condition2)
                 but the first if(condition1) is false then in that case compiler will check else if(condition2)
         */



        /*
        TERNARY OPERATOR

        NOTE: binary operator -  2 operands
              unary  operator -  1 operands
              ternary operator- 3 operands

        variable = condition? statement1 : statement2;
         */


        /*
        switch(variable){

        case 1 : sout();
        break;

        case 2 : sout();
        break;

        case 3 : sout();
        break;
        }

         */
    }
}
